/**
 * Prints a face using text characters
 * @author CloudStrife
 *
 */
public class FacePrinter
{

	/**
	 * @param args
	 */
	public static void main(String[] args)
	{
		/* Create the hair */
		System.out.println("  /////  ");
		
		/* Create the eyes */
		System.out.println(" | o o | ");
		
		/* Create the nose */
		System.out.println("(|  ^  |)");
		
		/* Create the mouth */
		System.out.println(" | \\_/ |");
		
		/* Create the chin */
		System.out.println("  _____  ");
		
	}

}
